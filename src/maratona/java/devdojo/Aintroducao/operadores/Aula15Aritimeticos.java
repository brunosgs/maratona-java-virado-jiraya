package maratona.java.devdojo.Aintroducao.operadores;


public class Aula15Aritimeticos {

	public static void main(String[] args) {
		// Operadores: soma (+), subtração(-), multiplicação(*) e divisão(/)
		int numero1 = 10;
		int numero2 = 20;

		int resultado = numero1 + numero2;

		System.out.println(numero1 + numero2);
		System.out.println(numero1 - numero2);
		System.out.println(numero1 * numero2);
		System.out.println(numero1 / numero2); // A divisão de 10 / 20 = 0, porque foi definido como um tipo inteiro

		System.out.println(resultado);
	}

}
